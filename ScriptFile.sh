#!/bin/bash
echo "=============================================================================================="
echo "************************Disk Usage complete details***********************************"
echo "$(df -h)"
echo "***************************Disk Usage complete details in ratio********************************/n"
df -h | awk '$NF=="/"{printf "Disk Usage: %d/%dGB (%s)\n", $3,$2,$5}'
echo "=============================================================================================="

echo "********************************CPU Usage-Complete details***************************"
echo "$(top -bn1)"
echo "***************************CPU Usage with Load details********************************/n"
top -bn1 | grep load | awk '{printf "CPU Load Info: %.2f\n", $(NF-2)}' 
echo "=============================================================================================="

echo "*************************Network Usage Details**********************************/n"
echo "Network Usage"
echo "$(sudo iftop -P -t -s 2 -n -N)"
